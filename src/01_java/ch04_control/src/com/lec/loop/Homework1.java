package com.lec.loop;
// 1부터 20까지 정수 중에서 2와 3의 배수가 아니 수의 합	
public class Homework1 {
	public static void main(String[] args) {
		int sum = 0;
		for(int i = 1; i<21; i++ ) {
			if(i % 2 != 0 && i % 3 != 0) {
				System.out.println(" 1부터 20까지의 정수 중에서 2와 3의 배수가 아닌 수의 합" + sum);
				sum = sum + i;	
			}
			
		}
		System.out.println("결과 : " + sum);
	}
}
